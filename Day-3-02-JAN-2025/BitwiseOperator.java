public class BitwiseOperator {
    public static void main(String[] args) {
        int a=5;
        int b=8;
        System.out.println(a|b);
        System.out.println(a&b);
        System.out.println(~a);
        System.out.println(a^b);
        System.out.println(a<<2); // a*2^b =20
        System.out.println(b>>2); // b/2^b = 2
    }
}
